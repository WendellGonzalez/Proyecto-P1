    private void cargarEspecialidadesEnCombo() {
        DefaultComboBoxModel<String> comboModel = new DefaultComboBoxModel<>();
        comboModel.addElement("SELECCIONA");
        especialidadMap.clear();

        EspecialidadDAO especialidadDAO = new EspecialidadDAOImpl();
        List<Especialidad> lista = especialidadDAO.obtenerTodas();

        for (Especialidad esp : lista) {
            comboModel.addElement(esp.getNombre());
            especialidadMap.put(esp.getNombre(), esp);
        }
        comboEspecialidad.setModel(comboModel);
    }
    
    public void registroGeneralMedicos() {

        try {
            if (medico == null) {
                medico = new Medico();
            }

            String especialidadNombre = (String) comboEspecialidad.getSelectedItem();

            if (especialidadNombre == null || especialidadNombre.equals("SELECCIONA")) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una especialidad", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Especialidad especialidadSeleccionada = especialidadMap.get(especialidadNombre);

            if (especialidadSeleccionada == null) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una especialidad", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String numeroColegiatura = txtNumeroColegiatura.getText().trim();
            String universidad = txtUniversidad.getText().trim();
            String aniosExperiencia = txtAniosExperiencia.getText().trim();
            String fechaGrad = txtFechaGraduacion.getText().trim();

            //Validar campos vacios
            if (numeroColegiatura.isEmpty() || universidad.isEmpty()
                    || aniosExperiencia.isEmpty() || fechaGrad.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Los campos deben estar completados", "Campos vacios", JOptionPane.WARNING_MESSAGE);
                return;
            }

            //Validar formato de fecha
            LocalDate fechaGraduacion;
            try {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                fechaGraduacion = LocalDate.parse(fechaGrad, formatter);

                if (fechaGraduacion.isAfter(LocalDate.now())) {
                    JOptionPane.showMessageDialog(this, "La fecha de graduacion no puede ser futura.", "Fecha InvÃ¡lida", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Formato de fecha invÃ¡lido. Usa yyyy-MM-dd", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Setear datos en el objeto medico
            medico.setEspecialidad(especialidadSeleccionada);
            medico.setIdEspecialidad(especialidadSeleccionada.getIdEspecialidad());
            medico.setNumeroColegiatura(numeroColegiatura);
            medico.setUniversidad(universidad);
            medico.setAniosExperiencia(aniosExperiencia);
            medico.setFechaGraduacion(fechaGraduacion);

            //Llamado para guardar el medico completo
            guardarMedicoCompleto();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    // Metodo para guardar Medico Completo
    public void guardarMedicoCompleto() {
        MedicoDAO dao = new MedicoDAOImpl();
        boolean exito = dao.registrar(medico);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Medico registrado exitosamente");
            new LOGIN().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar Medico", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

